{
    /**
     * {@inheritDoc}
     */
    protected function populateConfig(ArrayObject $config)
    {
        $config->defaults(
            [
            'payum.factory_name' => 'novalnet_multibanco',
            'payum.factory_title' => 'novalnet multibanco',
            ]
        );

        parent::populateConfig($config);
    }
}
